public class NewsHome {
	String id_NewsHome;
	String tieuDe;
	String noiDung;
	String image;
	public String getId_NewsHome() {
		return id_NewsHome;
	}
	public void setId_NewsHome(String id_NewsHome) {
		this.id_NewsHome = id_NewsHome;
	}
	public String getTieuDe() {
		return tieuDe;
	}
	public void setTieuDe(String tieuDe) {
		this.tieuDe = tieuDe;
	}
	public String getNoiDung() {
		return noiDung;
	}
	public void setNoiDung(String noiDung) {
		this.noiDung = noiDung;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	
}
